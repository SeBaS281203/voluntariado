// --- MÉTODO CORREGIDO PARA LLENAR LA TABLA ---
    private void cargarTabla() {
        // 1. Definimos los títulos de las columnas
        String[] titulos = {"ID", "Nombres", "Apellidos", "Correo", "Teléfono", "Carrera"};
        
        // 2. Creamos el modelo visual
        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(titulos, 0);
        
        // 3. Pedimos la lista a la base de datos (usando el servicio que ya tienes declarado)
        java.util.List<Entidades.Voluntario> listaVoluntarios = servicio.listarVoluntarios();
        
        // 4. Llenamos las filas
        for (Entidades.Voluntario v : listaVoluntarios) {
            Object[] fila = {
                v.getIdVoluntario(),
                v.getNombres(),
                v.getApellidos(),
                v.getCorreo(),
                v.getTelefono(),
                v.getCarrera()
            };
            modelo.addRow(fila);
        }
        
        // 5. Asignamos el modelo a la tabla visual
        tblVoluntarios.setModel(modelo);
    }
