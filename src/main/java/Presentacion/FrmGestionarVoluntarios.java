    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
// --- 1. CAPTURAR DATOS ---
        // Leemos lo que el usuario escribió en las cajas de texto
        String nom = txtNombres.getText();
        String ape = txtApellidos.getText();
        String mail = txtCorreo.getText();
        String tel = txtTelefono.getText();
        String carr = txtCarrera.getText();
        
        // --- 2. CREAR OBJETO ---
        // Empaquetamos esos datos en un objeto Voluntario.
        // El "0" es el ID de usuario (lo dejamos en 0 por ahora)
        // "Activo" es el estado por defecto.
        Voluntario v = new Voluntario(nom, ape, mail, tel, carr, "Activo", 0);
        
        // --- 3. ENVIAR A GUARDAR ---
        // Le decimos al servicio: "Valida y guarda esto por favor"
        if (servicio.registrarVoluntario(v)) {
            // Si devuelve TRUE, es que todo salió bien
            JOptionPane.showMessageDialog(this, "¡Voluntario registrado con éxito!");
            
            cargarTabla();   // Actualizamos la tabla para ver el nuevo registro
            limpiarCampos(); // Borramos los textos
            
        } else {
            // Si devuelve FALSE, algo falló (validación o base de datos)
            JOptionPane.showMessageDialog(this, 
                    "Error al guardar. Verifique que los campos obligatorios estén llenos.", 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

