    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
try {
            // 1. Validar que la fecha no esté vacía antes de convertirla
            if (txtFecha.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe ingresar una fecha.");
                return;
            }

            // 2. Recoger datos
            String nombre = txtNombre.getText();
            String desc = txtDescripcion.getText();
            String fechaTexto = txtFecha.getText().trim(); 
            System.out.println("INTENTANDO CONVERTIR: '" + fechaTexto + "'");

            // 3. Convertir texto a Fecha SQL (Aquí puede fallar si el formato está mal)
            Date fechaSQL = Date.valueOf(fechaTexto);
            
            // 4. Crear el objeto Actividad
            // (ID Beneficiario=0 y Usuario=1 son valores fijos temporales para probar)
            Actividad a = new Actividad(nombre, desc, fechaSQL, "Pendiente", 0, 1);
            
            // 5. Enviar a guardar
            if (servicio.registrarActividad(a)) {
                JOptionPane.showMessageDialog(this, "¡Actividad guardada con éxito!");
                cargarTabla();   // Refrescamos la tabla
                limpiarCampos(); // Limpiamos las cajas
            } else {
                JOptionPane.showMessageDialog(this, "Error al guardar. Revise los datos.");
            }
            
        } catch (IllegalArgumentException e) {
            // Este error salta si escriben la fecha mal (ej: "mañana" o "30-02-2024")
            JOptionPane.showMessageDialog(this, 
                "Formato de fecha incorrecto.\nUse: AAAA-MM-DD (Ej: 2024-12-31)", 
                "Error de Fecha", 
                JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

