    private void cargarTabla() {
        String[] titulos = {"ID", "Actividad", "Descripción", "Fecha", "Estado"};
        DefaultTableModel modelo = new DefaultTableModel(titulos, 0);
        
        // Traemos la lista de la BD
        List<Actividad> lista = servicio.listarActividades();
        
        for (Actividad a : lista) {
            Object[] fila = {
                a.getIdActividad(),
                a.getNombre(),
                a.getDescripcion(),
                a.getFecha(), // Java muestra la fecha SQL automáticamente
                a.getEstado()
            };
            modelo.addRow(fila);
        }
        tblActividades.setModel(modelo);
    }
    
